nei <- readRDS("summarySCC_PM25.rds")
n <- split(nei, as.factor(nei$year))
tot99 <- sum(n[["1999"]]$Emissions, na.rm = T)
tot02 <- sum(n[["2002"]]$Emissions, na.rm = T)
tot05 <- sum(n[["2005"]]$Emissions, na.rm = T)
tot08 <- sum(n[["2008"]]$Emissions, na.rm = T)
total <- c(tot99, tot02, tot05, tot08)
years <- c("1999", "2002", "2005", "2008")
par(mar = c(4, 4, 2, 2))
plot(years, total, ylab = "Total PM-2.5 Emissions", xlab = "Years", pch = 20)
dev.copy(png, file = "plot1.png")
dev.off()